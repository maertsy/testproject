package com.plant.power.PowerPlant.common;

/**
 * Class containing utility function
 *
 */
public class CommonUtility {

	/**
	 * Check if string is null or blank
	 * 
	 * @param str
	 * @return
	 */
	public static final boolean isNullOrBlank(String str) {
		return str == null || str.isBlank();
	}

}
